package be.lmenten.avr.core.descriptor;

public enum CoreMemory
{
	FLASH	( "flash" ),
	SRAM	( "sram" ),
	EEPROM	( "eeprom" ),
	;

	// ------------------------------------------------------------------------

	private final String name;

	// ========================================================================
	// ===
	// ========================================================================

	private CoreMemory( String name )
	{
		this.name = name;
	}

	// ========================================================================
	// ===
	// ========================================================================

	public String getName()
	{
		return name;
	}
}
